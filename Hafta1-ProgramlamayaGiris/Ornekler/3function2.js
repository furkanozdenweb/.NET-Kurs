

function Toplama(Deger1, Deger2) {
    return Deger1 + Deger2
}

function Cikartma(Deger1, Deger2) {
    return Deger1 - Deger2
}

function Bolme(Deger1, Deger2) {
    return Deger1 / Deger2
}

function Carpma(Deger1, Deger2) {
    return Deger1 * Deger2
}


IslemSonucu1 = Toplama(50, 35)
IslemSonucu2 = Cikartma(50, 35)
IslemSonucu3 = Bolme(50, 35)
IslemSonucu4 = Carpma(50, 35)

console.log("Toplamı: " + IslemSonucu1 + " Çıkartma: " + IslemSonucu2 + " Bolme: " + IslemSonucu3 + " Carpma: " + IslemSonucu4)



